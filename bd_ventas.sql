  `precioT` float NOT NULL,
  `fecha` date DEFAULT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci;

--
-- Disparadores `ventas`
--
DELIMITER $$
CREATE TRIGGER `reducir_stock_venta` AFTER INSERT ON `ventas` FOR EACH ROW BEGIN
    DECLARE v_tipo_id INT;
    DECLARE v_color_id INT;
    DECLARE v_material_id INT;
    DECLARE v_agregado_id INT DEFAULT NULL;  -- Default NULL para manejar productos sin agregado
    DECLARE v_current_stock INT;

    -- Obtener los IDs correspondientes a los nombres
    SELECT id INTO v_tipo_id FROM tipos WHERE nombre LIKE CONCAT(NEW.tipo, '%') LIMIT 1;
    SELECT id INTO v_color_id FROM colores WHERE nombre LIKE NEW.color LIMIT 1;
    SELECT id INTO v_material_id FROM materiales WHERE nombre LIKE NEW.material LIMIT 1;

    -- Manejar caso cuando agregado es NULL o vacío
    IF NEW.agregado IS NULL OR NEW.agregado = '' THEN
        SET v_agregado_id = -1;  -- Usamos un valor especial para indicar que no hay agregado
    ELSE
        SELECT id INTO v_agregado_id FROM agregados WHERE nombre LIKE NEW.agregado LIMIT 1;
    END IF;

    -- Verificar que los IDs sean válidos
    IF v_tipo_id IS NULL OR v_color_id IS NULL OR v_material_id IS NULL OR (v_agregado_id IS NULL AND NEW.agregado IS NOT NULL) THEN
        SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'Uno o más ID no son válidos';
    ELSE
        -- Obtener el stock actual del producto
        SELECT stock INTO v_current_stock
        FROM producto_combinaciones
        WHERE tipo_id = v_tipo_id
        AND color_id = v_color_id
        AND material_id = v_material_id
        AND (agregado_id = v_agregado_id OR (v_agregado_id = -1 AND agregado_id IS NULL));  -- Considerar NULL para productos sin agregado
        
        -- Guardar en la tabla de depuración para verificar los valores de stock (solo para depuración)
        -- INSERT INTO debug_logs (variable_name, variable_value) VALUES ('v_current_stock', v_current_stock);

        -- Verificar que el stock sea suficiente
        IF v_current_stock >= NEW.cantidad THEN
            -- Reducir el stock
            UPDATE producto_combinaciones
            SET stock = stock - NEW.cantidad
            WHERE tipo_id = v_tipo_id
            AND color_id = v_color_id
            AND material_id = v_material_id
            AND (agregado_id = v_agregado_id OR (v_agregado_id = -1 AND agregado_id IS NULL));
        ELSE
            -- Lanzar un error si el stock no es suficiente
            SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'Stock insuficiente para la venta';
        END IF;
    END IF;
END
$$
