  `id` int(11) NOT NULL,
  `tipo` varchar(200) DEFAULT NULL,
  `color` varchar(200) DEFAULT NULL,
  `material` varchar(200) DEFAULT NULL,
  `agregado` varchar(200) DEFAULT NULL,
  `cantidad` int(11) DEFAULT NULL,
